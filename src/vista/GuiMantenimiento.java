    private void tblMaterialMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMaterialMouseClicked

        int row = tblMaterial.getSelectedRow();
        if (row >= 0) {
            Material m = op.buscarMaterial(Integer.parseInt(String.valueOf(tblMaterial.getValueAt(row, 0))));
            if (m != null) {
                limpiarCajar();
                txtId.setText(m.getIdMaterial() + "");
                txtNombre.setText(m.getNombre());
                txtCantidad.setText(m.getCantidad());
                txtUbicacion.setText(m.getUbicacion());
                txtVolumen.setText(m.getVolumen());
                int cate = m.getCateSubCate().getCategoria().getIdCategoria();
                cboCategoria.setSelectedIndex(cate);
                switch (cate) {
                    case 1:
                        txtCiveq.setText(m.getCiveq());
                        txtkimax.setText(m.getKimax());
                        txtSinMarca.setText(m.getSinMarca());
                        txtPirex.setText(m.getPirex());
                        txtSimax.setText(m.getSimax());
                        txtBrand.setText(m.getBrand());
                        txtSchottDuran.setText(m.getSchott_duran());
                        txtPKmexica.setText(m.getPkMexico());
                        txtSuperiekBorosilicate.setText(m.getSuperiekborosilicate());
                        txtGlass.setText(m.getGlass());
                        txtP_E.setText(m.getP_e());
                        txtObservacion.setText(m.getObservaciones());
                        break;
                    case 3:
                        cboSubCategoria.setSelectedIndex(m.getCateSubCate().getSubCategoria().getSubCate());
                        break;
                }
                habilitarCaja(true);
                habilitarBotone(true, false, true, true);
            }

        }

    }//GEN-LAST:event_tblMaterialMouseClicked

