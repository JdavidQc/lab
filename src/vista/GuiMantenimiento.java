    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (tblMaterial.getSelectedRow() >= 0) {
            if (JOptionPane.showConfirmDialog(null, "Eliminar el material?", "Confirmar", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                boolean bandera = op.EliminarMaterial(Integer.parseInt(
                        String.valueOf(tblMaterial.getValueAt(tblMaterial.getSelectedRow(), 0))));
                if (bandera) {
                    cargarmaterial();
                    JOptionPane.showMessageDialog(null, "Se elimino correctamente");
                    limpiarCajar();
                    habilitarCaja(false);
                    habilitarBotone(true, false, false, false);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al eliminar");

                }
            } else {

                tblMaterial.setSelectionMode(-1);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un fila");

        }


    }//GEN-LAST:event_btnEliminarActionPerformed

