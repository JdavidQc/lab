    private void cargarCategoria() {
        cboCategoria1.addItem("Seleccione");
        op.listadoCategoria().forEach(cate -> {
            cboCategoria1.addItem(cate.getNombreCate());
        });
    }
    private void cargarmaterial() {
        cboCategoria1.setSelectedIndex(0);
        cboCategoria2.setSelectedIndex(0);
        txtBuscar.setText(null);
        dtm.setRowCount(0);
        op.listadoMaterial().forEach(m -> {
            dtm.addRow(new Object[]{m.getIdMaterial(),
                m.getNombre(), m.getVolumen(),
                m.getCantidad(), m.getUbicacion(),
                m.getObservaciones(),
                m.getCateSubCate().getCategoria().getNombreCate(),
                m.getCateSubCate().getSubCategoria().getNombSub()
            });
        });
    }

    private void cboCategoria1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCategoria1ActionPerformed
      if (cboCategoria1.getSelectedIndex() == 3) {
            cboSubCategoria.removeAllItems();
            cboSubCategoria.addItem("Seleccione");
            cboSubCategoria.setVisible(true);  jLabel1.setVisible(true);
            op.listadoSubCategoria().forEach(subCate -> {
                cboSubCategoria.addItem(subCate.getNombSub());
            });
            cboSubCategoria.setSelectedIndex(0);
        } else {
            cboSubCategoria.removeAllItems();
            cboSubCategoria.setVisible(false);
            jLabel1.setVisible(false);
        }
          dtm.setRowCount(0);
        op.listadoMaterial().forEach(m -> {
            if(m.getCateSubCate().getCategoria().getIdCategoria() == cboCategoria1.getSelectedIndex())
            dtm.addRow(new Object[]{m.getIdMaterial(),
                m.getNombre(), m.getVolumen(),
                m.getCantidad(), m.getUbicacion(),
                m.getObservaciones(),
                m.getCateSubCate().getCategoria().getNombreCate(),
                m.getCateSubCate().getSubCategoria().getNombSub()
            });
        });
    }//GEN-LAST:event_cboCategoria1ActionPerformed

