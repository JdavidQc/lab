    private void cboSubCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboSubCategoriaActionPerformed
     dtm.setRowCount(0);
        op.listadoMaterial().forEach(m -> {
            if(m.getCateSubCate().getCategoria().getIdCategoria() == cboCategoria1.getSelectedIndex()
                    && m.getCateSubCate().getSubCategoria().getSubCate() == cboSubCategoria.getSelectedIndex())
            dtm.addRow(new Object[]{m.getIdMaterial(),
                m.getNombre(), m.getVolumen(),
                m.getCantidad(), m.getUbicacion(),
                m.getObservaciones(),
                m.getCateSubCate().getCategoria().getNombreCate(),
                m.getCateSubCate().getSubCategoria().getNombSub()
            });
        });
    }//GEN-LAST:event_cboSubCategoriaActionPerformed

