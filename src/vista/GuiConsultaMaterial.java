	   TableRowSorter<DefaultTableModel> filtro;
	
		void Filtro(int column){
			txtBuscar.addKeyListener(new KeyAdapter() {
			
			@Override
			public void keyReleased( KeyEvent e) {
				filtro.setRowFilter(RowFilter.regexFilter(txtBuscar.getText().trim(),column));
				
			}});
			
			filtro=new TableRowSorter<DefaultTableModel>(dtm);
		tblMaterial.setRowSorter(filtro);
		}
	
